public class ThreeRebate {
    private double first;
    private double second;
    private double third;
    private double contribute;

    public double getFirst() {
        return first;
    }

    public ThreeRebate setFirst(double first) {
        this.first = first;
        return this;
    }

    public double getSecond() {
        return second;
    }

    public ThreeRebate setSecond(double second) {
        this.second = second;
        return this;
    }

    public double getThird() {
        return third;
    }

    public ThreeRebate setThird(double third) {
        this.third = third;
        return this;
    }

    public double getContribute() {
        return contribute;
    }

    public ThreeRebate setContribute(double contribute) {
        this.contribute = contribute;
        return this;
    }


}
